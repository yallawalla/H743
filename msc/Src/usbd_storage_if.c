/**
  * @brief  Initializes over USB FS IP
  * @param  lun:
  * @retval USBD_OK if all operations are OK else USBD_FAIL
  */
int8_t STORAGE_Init_FS(uint8_t lun)
{
  /* USER CODE BEGIN 2 */
  return USER_initialize(lun);
  /* USER CODE END 2 */
}

/**
  * @brief  .
  * @param  lun: .
  * @param  block_num: .
  * @param  block_size: .
  * @retval USBD_OK if all operations are OK else USBD_FAIL
  */
int8_t STORAGE_GetCapacity_FS(uint8_t lun, uint32_t *block_num, uint16_t *block_size)
{
  /* USER CODE BEGIN 3 */
	uint32_t blocksize,blocknum;
	USER_ioctl(lun,GET_SECTOR_SIZE,&blocksize);
	USER_ioctl(lun,GET_SECTOR_COUNT,&blocknum);
  *block_num  = blocknum;
  *block_size = blocksize;
  return (USBD_OK);
  /* USER CODE END 3 */
}

